<div class="well well-small">
<strong>POST-WAXING SKINCARE</strong><br><br>
	<ul>
    	<li>Use Neosporin, Aloe Vera or Hydrocortisone as a topical application during healing process</li>
        <li>Avoid tight fitting garments for 24-48 hours after body waxing.</li>
        <li>Keep the waxed area clean, but do not use harsh cleaners or creams for 24-48 hours post- waxing.</li>
        <li>Dab on Hydrogen peroxide every 6 hours for 24 hours post- waxing </li>
        <li>Do not touch the waxed areas with fingers or pick at ingrown hairs</li>
        <li>Do not exfoliate for 2-3 days post waxing, and then exfoliate regularly to avoid ingrown hairs</li>
        <li>No Peels or microdermabrasion, NO hot tubs, hot showers or baths for 24-48 hours post - waxing</li>
        <li>Avoid chlorine for 4 hours post-waxing</li>
        <li>Avoid shaving between waxes.</li>
    </ul>
