<div class="well well-small">
<strong>We DO NOT Recommend waxing if you are using the following:</strong>
	<ul>
    	<li>Retin A/Renova - 30 days of non-use required to wax</li>
        <li>Accutane - 90 days of non-use require to wax</li>
        <li>Differen Gel - 14 days of non-use</li>
        <li>Metro-Gel - 14 days of non-use.</li>
