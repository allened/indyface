<div>
<strong>The science behind LATISSE&#174;.</strong>
</div>
<p>
LATISSE&#174; makes lash growth possible because of its active ingredient: bimatoprost. Although the precise mechanism of action is unknown, LATISSE&#174; is believed to affect the growth (anagen) phase of the eyelash hair cycle in two ways: first, it increases the length of this phase, and second, it increases the number of hairs in this growth phase.
</p>

<div>
<strong>It's a product whose time has come.</strong>
</div>
<p>
In 2001, Allergan developed a medicated eye drop used to lower eye pressure (known as IOP or intraocular pressure) in patients with open-angle glaucoma or ocular hypertension. As a result, patients using this medication began experiencing eyelash growth as a side effect. This led Allergan, already a pioneer in eye care, dermatological and aesthetic products, to begin studying its active ingredient. After a clinical trial, LATISSE&#174; earned its FDA approval in December 2008. And since then, approximately 1.5 million bottles of LATISSE&#174; have been prescribed to approximately 350,000 people.
</p>