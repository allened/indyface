<h3>
Frequent Questions About Plastic Surgery Financing
</h3>

<p>
Cosmetic plastic surgery financing can help you make your dream procedure a reality. But understanding the ins and outs of surgery financing isn't always easy. The list of questions below were submitted by men and women just like you. If your questions aren't answered by this page, please don't hesitate to <a href="http://www.financing-plastic-surgery.com/contact.cfm">e-mail us</a>. </p>

<p>
<strong>I don't think I will be approved for all the money I need. Can I apply for half of what I need at two different companies?</strong><br>
If you know your credit is poor, keep in mind that applying for loans actually will make it work. When you apply for the first loan your credit score will drop a bit so you are less likely to be approved on the next loan. This is to protect you from digging a hole too deep to get out of, and to protect the banks from helping you get over extended. So, while there's a chance you could pull this off, it's not likely to work.
</p>

<p>
<strong>Do all doctors accept financing?</strong><br>
No. Our office is happy to help you with various financing options though.
</p>

<p>
<strong>Do I have to borrow the full price of my procedure?</strong><br>
No. Most <a href="http://www.financing-plastic-surgery.com/financing-companies.cfm">plastic surgery finance companies</a> will loan you as little as $2000 or as much as $20,000, depending on your credit history. In general, however, the less you ask for the more likely you are to be approved. Many people choose to pay for part of their plastic surgery in cash or on a credit card and then borrow the rest. </p>

<p>
<strong>Do I have to provide collateral for my plastic surgery financing?</strong><br>
Unlike home and auto loans, plastic surgery loans are unsecured personal loans (similar to a credit card). 'Unsecured' means that you do not have to offer collateral for the loan, and that there is nothing to repossess. That is actually not a good thing. Since there is nothing for the plastic surgery finance company or bank to repossess, they have no way to recover their loan if you stop paying. As a result, their risk is higher and the interest rates for unsecured loans are also usually higher.</p>

<p>
<strong>Do I have to select a plastic surgeon before I apply for financing?</strong><br>
While it is not required, it is a good idea to select your surgeon before you apply. Either way you can chose a surgeon that accepts financing from our national directory. </p>

<p>
<strong>How do I know if I am approved?</strong><br>
When you apply online most plastic surgery finance applications are programmed to respond immediately with your approval status. The status page that appears after you submit your application will usually tell you if you are approved, denied, or if the bank needs more information to make the decision. In any case, you will almost always be provided with a reference number, a telephone number, and instructions on how to proceed.</p>

<p>
<strong>Do I have to spend every penny of my loan?</strong><br>
Typically, no. People with great credit are often approved with a credit limit that exceeds the cost of their surgery. Most plastic surgery finance companies will allow you to borrow just what you need, even if you get approved for more.</p>