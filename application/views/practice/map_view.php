<p>
	<span class="title2">Directions:</span>
</p>
<p>
Address: 2000 E. 116th St., Carmel, IN 46032
</p>
<p>
<strong>From the north:</strong><br>
Take US 31 (Meridian) to 116th St. Turn left (east). Go to the stoplight at AAA Way and turn left. Our building is immediately off the street.
</p>

<p>
<strong>From the south:</strong><br>
Take US 31 to 116th St. Turn right (east). Go to the stoplight at AAA Way and turn left. Our building is immediately off the street.
</p>

<p>
<strong>From the east:</strong><br>
Take 116th St. across Keystone to the next stoplight (AAA Way). Turn right. Our building is immediately off the street. If the 116th exit off Keystone is closed, go to 126th street and turn left. Go to Rangeline Road and turn left (south).  Continue until 116th and turn left (east). Go to the stoplight at AAA Way and turn left. Our building is immediately off the street.
</p>

<p>
<strong>From the west:</strong><br>
Take 116th St across Meridian. Continue going east until the stoplight at AAA Way. Turn left. Our building is immediately  off the street 
</p>

