    <div class="span11">
        <p>
            <strong>
            I have a saggy jawline and my upper eyelids are baggy, is there one surgery that will fix all the droopy areas?
            </strong>
        </p>
        <p>
        The "old" facelift used to attempt to fix all droopiness at once. The result was a very pulled, "operated-on" look. Newer procedures are geared to addressing individual areas of the face. Each area and problem should be addressed independently to achieve the most natural and refreshed look;<br>
    UPPER FACE (usually treated with an endoscopic browlift and/or upper eyelid lift)<br>
    MID-FACE/LOWER EYELID (endoscopic or standard midface/lower eyelift)<br>
    LOWER FACE/JAWLINE/NECK (facelift, "tuck-up" or neck lift)<br>
    WRINKLES AROUND MOUTH/LIPS (injectables and implants)<br>
    Individuals "tweaks" can be discussed, such as facial resurfacing, Threadlifting and other minimally invasive procedures that help in achieving the desired look.
        </p>
