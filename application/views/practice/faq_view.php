    <div class="span11">
        <p>
            <strong>
          What does Board Certification mean?
            </strong>
        </p>
        <p>
Board Certification is a several day process of written and oral examinations that ensures your physician is qualified to be performing procedures. Many physicians performing cosmetic procedures and even surgeries are trained and board certified in Internal Medicine, Dermatology or other non-surgical specialties. Be sure to ask WHICH board has certified them! Board certification in plastic surgery (ABPS) means a surgeon has completed an approved plastic surgery training (although the experience in each training program will vary) and has passed examinations. These are the ONLY surgeons who should be performing body liposuction, tummy tucks, breast augmentation, and body procedures.<br>
Plastic surgery is becoming more subspecialized, and facial cosmetic surgery is increasingly being referred to FACIAL plastic surgeons who have more experience in this area. Someone who specializes in everything specializes in nothing- if a surgeon advertises subspecialty focus in facial plastics, breast and body plastics and hand plastics there is no true subspecialty focus. For FACIAL plastic surgery, look for surgeons board certified through the American Board of Facial Plastic and Reconstructive Surgery. The ABFPRS is the only plastic surgery sub-specialty board that is ABMS equivalent and recognized in every US state. Board certification in FACIAL plastic surgery usually comes through completion of a fellowship that specializes in ONLY facial plastic and reconstructive surgery. Applicants can only be surgeons- head and neck (ENT) or plastic surgeons. Additionally, applicants MUST have a minimum number of facial plastic surgeries to become certified, as well as pass the oral and written examination. It is an arduous process that ensures that the surgeons operating on such a delicate area have the most experience possible. Again, the biggest difference with the ABFPRS is that facial plastics EXPERIENCE is a REQUIREMENT for certification, meaning your face will not be in the hands of the "occasional" facial cosmetic surgeon who largely focuses on breasts and tummy tucks.<br>
